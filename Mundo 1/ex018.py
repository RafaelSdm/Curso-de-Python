from math import  radians,cos,tan,sin
angulo =float(input("digite um angulo qualquer:"))
tangente = tan(radians(angulo))
cosseno = cos(radians(angulo))
seno = sin(radians(angulo))
print(" a tangente vale {}".format(tangente))
print("o cosseno vale {}".format(cosseno))
print("o seno vale {}".format(seno))